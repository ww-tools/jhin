package org.willwin.jhin.domain.riot.match;

/**
 * Enum representing the individual position of a participant in a League of Legends match.
 */
public enum IndividualPosition
{
    /**
     * Top lane position.
     */
    TOP,

    /**
     * Jungle position.
     */
    JUNGLE,

    /**
     * Middle lane position.
     */
    MIDDLE,

    /**
     * Bottom lane position (ADC).
     */
    BOTTOM,

    /**
     * Utility position (Support).
     */
    UTILITY,

    /**
     * Invalid or unassigned position.
     */
    INVALID
}
