package org.willwin.jhin.model.domain.match;

/**
 * Enum representing the type of level up that occurred in a skill level up event.
 */
public enum LevelUpType
{
    /**
     * Normal skill level up.
     */
    NORMAL,

    /**
     * Evolve level up (for champions like Kha'Zix, Kayn).
     */
    EVOLVE
}
