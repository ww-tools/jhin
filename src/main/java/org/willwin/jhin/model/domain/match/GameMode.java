package org.willwin.jhin.model.domain.match;

/**
 * Enum representing the game mode played in a League of Legends match.
 */
public enum GameMode
{
    /**
     * Classic mode - Summoner's Rift 5v5.
     */
    CLASSIC,

    /**
     * ARAM - All Random All Mid on Howling Abyss.
     */
    ARAM,

    /**
     * URF - Ultra Rapid Fire mode.
     */
    URF,

    /**
     * Nexus Blitz - fast-paced game mode.
     */
    NEXUSBLITZ,

    /**
     * One For All - all players play the same champion.
     */
    ONEFORALL,

    /**
     * Ascension - Crystal Scar game mode.
     */
    ASCENSION,

    /**
     * King of the Hill - Twisted Treeline mode.
     */
    KINGPORO,

    /**
     * Odyssey - PvE game mode.
     */
    ODYSSEY,

    /**
     * Tutorial mode.
     */
    TUTORIAL
}
